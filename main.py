def future(grid, size):
    future_grid = [[0 for i in range(size)] for j in range(size)]

    for m in range(size):
        for n in range(size):

            alive_neighbours = 0
            for i in range(-1, 2):
                for j in range(-1, 2):
                    if (0 <= m + i < size) and (0 <= n + j < size):
                        alive_neighbours += grid[m + i][n + j]

            alive_neighbours -= grid[m][n]
            # pois, estávamos contando o espaço do meio também, que não pode ser considerado um vizinho7

            # Célula viva sozinha / baixa população morre
            if (grid[m][n] == 1) and (alive_neighbours < 2):
                future_grid[m][n] = 0

            # Célula viva morre de superpopulação
            elif (grid[m][n] == 1) and (alive_neighbours > 3):
                future_grid[m][n] = 0

            # Nascimento
            elif (grid[m][n] == 0) and (alive_neighbours == 3):
                future_grid[m][n] = 1

            # Continua do mesmo jeito
            else:
                future_grid[m][n] = grid[m][n]
    return future_grid


def next_gen(grid, size):
    os.system("clear")
    for i in range(size):
        for j in range(size):
            if grid[i][j] == 0:
                print(".", end="  ")
            else:
                print("*", end="  ")
        print("\n")
    time.sleep(0.5)
    future_grid = future(grid, size)
    next_gen(future_grid, size)


